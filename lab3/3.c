	printf ("%c %6c %6s %14.1f| %14.1f| %14.1f|\n",'X','y',"z=",x,y,z );
	printf ("%c %6c %6s %14.1f| %14.1f| %14.1f|\n",'A','B',"C=",A,B,C );
